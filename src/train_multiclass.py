import torch
import torch.nn as nn
import torch.optim as optim
from torch.utils.data import DataLoader, random_split
from sklearn.metrics import accuracy_score, f1_score, roc_auc_score
import numpy as np
import argparse
import os
import json
import wandb
from tqdm import tqdm
from torch.cuda.amp import autocast, GradScaler

from src.dataset import HarmonizedDataset
from src.models.wavkan import WavKANClassifier
from src.models.baselines import ResNet1D, ViT1D, SimpleMLP, InceptionTime, XResNet1D
from src.models.spline_kan import SplineKANClassifier
from src.losses import MultilabelFocalLoss
from src.utils import set_seed, get_device, save_checkpoint, log_gpu_memory, compute_pos_weight

def train_epoch(model, loader, criterion, optimizer, scaler, device, max_grad_norm=1.0):
    model.train()
    running_loss = 0.0
    all_preds = []
    all_labels = []
    
    for inputs, labels in tqdm(loader, desc="Training", leave=False):
        inputs, labels = inputs.to(device).float(), labels.to(device).float()
        
        optimizer.zero_grad()
        
        with autocast():
            outputs = model(inputs)
            loss = criterion(outputs, labels)
            
        scaler.scale(loss).backward()
        
        scaler.unscale_(optimizer)
        torch.nn.utils.clip_grad_norm_(model.parameters(), max_grad_norm)
        
        scaler.step(optimizer)
        scaler.update()
        
        running_loss += loss.item()
        
        # Sigmoid for multi-label BCE
        probs = torch.sigmoid(outputs).detach().cpu().numpy()
        preds = (probs > 0.5).astype(int)
        
        all_preds.extend(preds)
        all_labels.extend(labels.cpu().numpy())
    
    # Sample-averaged F1 and Macro F1
    # We report Macro F1 as the primary metric
    f1_macro = f1_score(all_labels, all_preds, average='macro', zero_division=0)
    
    return running_loss / len(loader), f1_macro

def evaluate(model, loader, device):
    model.eval()
    all_preds = []
    all_probs = []
    all_labels = []
    
    with torch.no_grad():
        for inputs, labels in tqdm(loader, desc="Evaluating", leave=False):
            inputs, labels = inputs.to(device).float(), labels.to(device).float()
            
            with autocast():
                outputs = model(inputs)
            
            probs = torch.sigmoid(outputs).cpu().numpy()
            preds = (probs > 0.5).astype(int)
            
            all_probs.extend(probs)
            all_preds.extend(preds)
            all_labels.extend(labels.cpu().numpy())
            
    f1_macro = f1_score(all_labels, all_preds, average='macro', zero_division=0)
    
    try:
        auroc_macro = roc_auc_score(all_labels, all_probs, average='macro', multi_class='ovr')
    except ValueError:
        auroc_macro = 0.0 # Thrown if a class is missing from val set
        
    return f1_macro, auroc_macro


def main(args):
    set_seed(args.seed)
    device = get_device()

    num_classes = 5 # 5 superclasses

    # Track experiments
    wandb.init(
        project="wavkan-cl",
        config=vars(args),
        name=f"multiclass_{args.model}_{args.seed}",
        tags=["baseline", args.model, "multiclass"]
    )

    # Label paths generated by multilead pipeline
    source_label_path = args.source_file.replace('signals', 'labels') if '.npy' in args.source_file else None
    target_label_path = args.target_file.replace('signals', 'labels') if '.npy' in args.target_file else None

    # Load datasets
    source_dataset = HarmonizedDataset(args.source_file, label_path=source_label_path)
    target_dataset = HarmonizedDataset(args.target_file, label_path=target_label_path)

    # Split target into train/val for fine-tuning validation
    # (Here we use 20% target just for val metrics comparison, no training on target)
    target_train_size = int(0.8 * len(target_dataset))
    target_val_size = len(target_dataset) - target_train_size
    _, target_val = random_split(target_dataset, [target_train_size, target_val_size])

    source_loader = DataLoader(source_dataset, batch_size=args.batch_size, shuffle=True, pin_memory=True)
    target_val_loader = DataLoader(target_val, batch_size=args.batch_size, shuffle=False, pin_memory=True)

    # Initialize model
    if args.model == 'wavkan':
        model = WavKANClassifier(input_dim=1000, num_classes=num_classes, hidden_dim=args.hidden_dim, in_channels=args.in_channels).to(device)
    elif args.model == 'spline_kan':
        model = SplineKANClassifier(input_dim=1000, num_classes=num_classes, hidden_dim=args.hidden_dim, in_channels=args.in_channels).to(device)
    elif args.model == 'resnet':
        model = ResNet1D(in_channels=args.in_channels, num_classes=num_classes, seq_len=1000).to(device)
    elif args.model == 'vit':
        model = ViT1D(seq_len=1000, num_classes=num_classes, in_channels=args.in_channels).to(device)
    elif args.model == 'simple_mlp':
        model = SimpleMLP(input_dim=1000, num_classes=num_classes, in_channels=args.in_channels).to(device)
    elif args.model == 'inception':
        model = InceptionTime(in_channels=args.in_channels, num_classes=num_classes).to(device)
    elif args.model == 'xresnet':
        model = XResNet1D(in_channels=args.in_channels, num_classes=num_classes).to(device)

    if hasattr(source_dataset, 'y') and source_dataset.y is not None:
        y_tensor = torch.tensor(source_dataset.y, dtype=torch.float32)
        pos_weight = compute_pos_weight(y_tensor, device)
    else:
        pos_weight = None

    # Use Multilabel Focal Loss
    criterion = MultilabelFocalLoss(gamma=2.0, pos_weight=pos_weight)
    optimizer = optim.Adam(model.parameters(), lr=args.lr, weight_decay=args.weight_decay)
    
    # Cosine Annealing LR scheduler
    scheduler = torch.optim.lr_scheduler.CosineAnnealingLR(optimizer, T_max=args.epochs, eta_min=1e-6)
    scaler = GradScaler()

    best_f1 = 0.0
    patience_counter = 0

    checkpoint_path = f'experiments/checkpoints/multiclass_{args.model}_seed{args.seed}_best.pt'

    for epoch in range(args.epochs):
        train_loss, train_f1 = train_epoch(model, source_loader, criterion, optimizer, scaler, device)
        val_f1_macro, val_auroc_macro = evaluate(model, target_val_loader, device)
        
        scheduler.step()

        print(f"Epoch {epoch+1}/{args.epochs} - Train Loss: {train_loss:.4f}, Train F1: {train_f1:.4f}, Val F1 Macro: {val_f1_macro:.4f}, Val AUROC: {val_auroc_macro:.4f}")
        
        wandb.log({
            "epoch": epoch,
            "train_loss": train_loss,
            "train_f1": train_f1,
            "val_f1_macro": val_f1_macro,
            "val_auroc_macro": val_auroc_macro,
            "lr": optimizer.param_groups[0]['lr']
        })

        if val_f1_macro > best_f1:
            best_f1 = val_f1_macro
            patience_counter = 0
            
            save_checkpoint(model, optimizer, epoch, 
                            {'val_f1_macro': val_f1_macro, 'val_auroc_macro': val_auroc_macro}, 
                            vars(args), checkpoint_path)
        else:
            patience_counter += 1
            if patience_counter >= args.patience:
                print("Early stopping")
                break

    # Evaluate best model on full target
    checkpoint = torch.load(checkpoint_path)
    model.load_state_dict(checkpoint['model_state_dict'])
    
    target_full_loader = DataLoader(target_dataset, batch_size=args.batch_size, shuffle=False)
    final_f1_macro, final_auroc_macro = evaluate(model, target_full_loader, device)

    # Save final results run JSON
    results = {
        'config': vars(args),
        'metrics': {
            'final_f1_macro': final_f1_macro,
            'final_auroc_macro': final_auroc_macro,
            'best_val_f1': best_f1
        },
        'status': 'completed'
    }

    run_path = f'experiments/runs/multiclass_{args.model}_seed{args.seed}.json'
    os.makedirs(os.path.dirname(run_path), exist_ok=True)
    with open(run_path, 'w') as f:
        json.dump(results, f, indent=2)

    print(f"Final F1 Macro: {final_f1_macro:.4f}, AUROC Macro: {final_auroc_macro:.4f}")
    wandb.finish()


if __name__ == "__main__":
    parser = argparse.ArgumentParser(description='Train multi-class/multi-label ECG classification')
    parser.add_argument('--source_file', type=str, required=True, help='Path to source domain .npy')
    parser.add_argument('--target_file', type=str, required=True, help='Path to target domain .npy')
    parser.add_argument('--model', type=str, choices=['wavkan', 'spline_kan', 'resnet', 'vit', 'simple_mlp'], required=True)
    parser.add_argument('--hidden_dim', type=int, default=64, help='Hidden dimension for KAN models')
    parser.add_argument('--in_channels', type=int, default=12, help='Number of input channels (leads)')
    parser.add_argument('--batch_size', type=int, default=32)
    parser.add_argument('--lr', type=float, default=1e-3)
    parser.add_argument('--weight_decay', type=float, default=1e-4)
    parser.add_argument('--epochs', type=int, default=50)
    parser.add_argument('--patience', type=int, default=10)
    parser.add_argument('--seed', type=int, default=42)

    args = parser.parse_args()
    main(args)